export const drumMachineNames: string[] = Object.values(drumMachines).map(
  (drumMachine) => drumMachine.name
);

export const getDrumMachineKeyByName = (name: string): DrumMachineId => {
  for (const [key, value] of Object.entries(drumMachines)) {
    if (value.name === name) {
      return key as DrumMachineId;
    }
  }
  throw new Error(`Drum machine with name "${name}" not found.`);
};

export const getKitSampleTitle = (fileName: string) => {
  const title = fileName.split(".")[0].split("__")[1].replace("_", " ");
  return title;
};
